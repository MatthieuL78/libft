#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int i;

	i = 0;
	while (*(char*)s != '\0')
	{
		s++;
		i++;
	}
	while (i != 0)
	{
		if (*(char*)s == c)
			return ((char*)s);
		s--;
		i--;
	}
	return (NULL);
}
