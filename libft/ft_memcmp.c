#include "libft.h"

int		ft_memcmp(const void *s1, const void *s2, size_t n)
{
	while (s1 && s2 && n)
	{
		if (*(uint8_t*)s1 != *(uint8_t*)s2)
			return (*(uint8_t*)s1 - *(uint8_t*)s2);
		s1++;
		s2++;
		n--;
	}
	if (s1 == NULL || s2 == NULL)
		*(uint8_t*)s1 = *(uint8_t*)s2;
	return (0);
}
