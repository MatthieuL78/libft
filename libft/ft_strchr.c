#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	if (*(uint8_t*)s == '\0' && c > 0)
		return (NULL);
	while (*s != '\0')
	{
		if (*(uint8_t*)s == c)
			return ((char *)s);
		s++;
	}
	if (c == '\0')
		return ((char *)s);
	return (NULL);
}
